// @title          Staticlint
// @version        1.0
// @description    Lint contains analyzers
// @contact.name   Aynur Shauerman
// @contact.email  aykuli@ya.ru
// @license.name   MIT
// @license.url    http://www.apache.org/licenses/LICENSE-2.0.html
func main() {
	asasalinter, err := asasalint.NewAnalyzer(asasalint.LinterSetting{
		Exclude:             nil,
		NoBuiltinExclusions: false,
		IgnoreTest:          false,
	})
	if err != nil {
		panic(err)
	}

	saAnalyzers := []*analysis.Analyzer{
		appends.Analyzer,
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		framepointer.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		noosexit.Analyzer,
		printf.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		tests.Analyzer,
		testinggoroutine.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		// external checker
		asasalinter,       // Check for pass []any as any in variadic func(...any).
		errcheck.Analyzer, // errcheck is a program for checking for unchecked errors in Go code.
	}

	// appending SAxxxx analyzers
	for _, v := range staticcheck.Analyzers {
		saAnalyzers = append(saAnalyzers, v.Analyzer)
	}

	multichecker.Main(saAnalyzers...)
}
